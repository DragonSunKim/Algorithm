# 문제

# 일반 컵케이크 박스에는 8개의 컵케이크가, 작은 컵케이크 박스에는 3개의 컵케이크가 들어갑니다.
#  반에는 28명의 학생이 있고 최소 28개 이상의 컵케이크가 있습니다. 
# 각 학생이 하나의 컵케이크를 가져가고 난 후, 몇 개의 컵케이크가 남는지 구해봅시다.

# 입력

# 입력은 두 줄로 주어집니다.

# - 첫 줄에는 일반 컵케이크 박스의 수를 나타내는 정수 R (R >= 0)이 주어집니다.

# - 둘째 줄에는 작은 컵케이크 박스의 수를 나타내는 정수 S (S >= 0)가 주어집니다.

# 출력

# 남은 컵케이크의 수를 출력합니다.


R = int(input())
S = int(input())

ans = R*8 + S*3

print(ans-28)